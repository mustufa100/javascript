// 13. Program to take user input for a username and check for special symbols
let username = prompt("Enter a username:");

// Function to check if the username contains special symbols
function isValidUsername(username) {
  for (let i = 0; i < username.length; i++) {
    let charCode = username.charCodeAt(i);
    if (charCode === 33 || charCode === 44 || charCode === 46 || charCode === 64) {
      return false; // Username contains a special symbol
    }
  }
  return true; // Username is valid
}

// Check and prompt the user for a valid username
if (isValidUsername(username)) {
  console.log(`Username "${username}" is valid.`);
} else {
  console.log(`Invalid username. Please enter a valid username without special symbols.`);
}


