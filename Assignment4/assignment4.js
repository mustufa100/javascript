// Function to perform case-insensitive search in the array
function isItemInArray(item, array) {
  // Convert both the item and array elements to lowercase for case-insensitive search
  item = item.toLowerCase();
  array = array.map(element => element.toLowerCase());

  // Check if the item is in the array
  return array.includes(item);
}

